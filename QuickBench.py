# Packages required to run QuickBench.
# Each entry: (import_name, pip_name, why_needed)
# Note: Pillow is NOT required — the app icon is embedded as a base64 PNG
# and loaded natively by tk.PhotoImage (supported in Tk 8.6+ without extras).
_REQUIRED_PACKAGES = [
    ("psutil", "psutil", "CPU / RAM monitoring during benchmarks"),
]


def _check_missing():
    """Return list of (import_name, pip_name, reason) for uninstalled packages."""
    missing = []
    for import_name, pip_name, reason in _REQUIRED_PACKAGES:
        try:
            __import__(import_name)
        except ImportError:
            missing.append((import_name, pip_name, reason))
    return missing


def _install_packages(pip_names, progress_var, status_var, root):
    """Install packages in a thread and update the progress dialog."""
    total = len(pip_names)
    for i, name in enumerate(pip_names, 1):
        status_var.set(f"Installing {name}  ({i}/{total})…")
        try:
            subprocess.run(
                [sys.executable, "-m", "pip", "install", "--quiet", name],
                check=True,
                capture_output=True,
            )
        except subprocess.CalledProcessError as exc:
            root.after(0, lambda e=exc, n=name: messagebox.showerror(
                "Install Failed",
                f"Could not install '{n}':\n\n{e.stderr.decode(errors='replace').strip()}\n\n"
                "Try running:  pip install " + n
            ))
            root.after(0, root.destroy)
            return
        progress_var.set(int(i / total * 100))

    # All done — show restart notice, then close.
    def _show_restart():
        status_var.set("Installation complete!")
        progress_var.set(100)
        messagebox.showinfo(
            "Setup Complete — Restart Required",
            "All packages were installed successfully.\n\n"
            "Please restart QuickBench for the changes to take effect.",
        )
        root.destroy()

    root.after(0, _show_restart)


def bootstrap_dependencies():
    """
    Check for missing packages and, with the user's permission, install them.
    Shows a clean Tk dialog — no terminal interaction required.
    Returns True if the app may proceed, False if the user declined or an
    error occurred.
    """
    missing = _check_missing()
    if not missing:
        return True     # nothing to do

    # Build a small standalone Tk window (not the main app window).
    root = tk.Tk()
    root.withdraw()     # hide until we've positioned the dialog

    # --- Permission dialog ---------------------------------------------------
    ask = tk.Toplevel(root)
    ask.title("QuickBench — First-Time Setup")
    ask.resizable(False, False)

    # Center on screen
    ask.update_idletasks()
    w, h = 480, 300
    sx = (ask.winfo_screenwidth()  - w) // 2
    sy = (ask.winfo_screenheight() - h) // 2
    ask.geometry(f"{w}x{h}+{sx}+{sy}")

    # Prevent closing the dialog by accident
    ask.protocol("WM_DELETE_WINDOW", lambda: None)

    pad = dict(padx=24, pady=8)

    tk.Label(ask, text="QuickBench — First-Time Setup",
             font=("Helvetica", 14, "bold") if sys.platform == "darwin"
             else ("Segoe UI", 14, "bold") if os.name == "nt"
             else ("Sans", 14, "bold")).pack(pady=(20, 4))

    tk.Label(ask,
             text="The following Python packages are required\n"
                  "and are not yet installed on this machine:",
             font=("Helvetica", 11) if sys.platform == "darwin"
             else ("Segoe UI", 11) if os.name == "nt"
             else ("Sans", 11),
             justify="center").pack(**pad)

    # List the missing packages
    pkg_text = "\n".join(
        f"  • {pip_name}  —  {reason}"
        for _, pip_name, reason in missing
    )
    tk.Label(ask, text=pkg_text,
             font=("Courier New", 10),
             justify="left",
             anchor="w").pack(padx=24, pady=(0, 8), fill="x")

    tk.Label(ask,
             text="Install them now using pip?",
             font=("Helvetica", 11) if sys.platform == "darwin"
             else ("Segoe UI", 11) if os.name == "nt"
             else ("Sans", 11)).pack(**pad)

    user_agreed = tk.BooleanVar(value=False)

    btn_frame = tk.Frame(ask)
    btn_frame.pack(pady=(4, 20))

    def _agree():
        user_agreed.set(True)
        ask.destroy()

    def _decline():
        user_agreed.set(False)
        ask.destroy()

    # On macOS the Aqua theme ignores bg/fg on tk.Button entirely, rendering
    # the button white with invisible white text. Use ttk.Button on macOS
    # (which respects the native theme) and styled tk.Button elsewhere.
    if sys.platform == "darwin":
        ttk.Button(btn_frame, text="Install",
                   width=12, command=_agree).pack(side="left", padx=8)
        ttk.Button(btn_frame, text="Cancel",
                   width=12, command=_decline).pack(side="left", padx=8)
    else:
        tk.Button(btn_frame, text="Install",
                  width=12, command=_agree,
                  bg="#0A84FF", fg="white",
                  activebackground="#0060D0", activeforeground="white",
                  relief="flat", cursor="hand2").pack(side="left", padx=8)
        tk.Button(btn_frame, text="Cancel",
                  width=12, command=_decline,
                  relief="flat", cursor="hand2").pack(side="left", padx=8)

    ask.grab_set()
    ask.wait_window()

    if not user_agreed.get():
        root.destroy()
        return False

    # --- Progress dialog -----------------------------------------------------
    prog_win = tk.Toplevel(root)
    prog_win.title("Installing…")
    prog_win.resizable(False, False)
    prog_win.protocol("WM_DELETE_WINDOW", lambda: None)  # not dismissible

    pw, ph = 400, 130
    px = (prog_win.winfo_screenwidth()  - pw) // 2
    py = (prog_win.winfo_screenheight() - ph) // 2
    prog_win.geometry(f"{pw}x{ph}+{px}+{py}")

    status_var   = tk.StringVar(value="Starting…")
    progress_var = tk.IntVar(value=0)

    tk.Label(prog_win, textvariable=status_var,
             font=("Helvetica", 11) if sys.platform == "darwin"
             else ("Segoe UI", 11) if os.name == "nt"
             else ("Sans", 11)).pack(padx=20, pady=(18, 8))

    ttk.Progressbar(prog_win, variable=progress_var,
                    maximum=100, length=360).pack(padx=20, pady=(0, 18))

    pip_names = [pip_name for _, pip_name, _ in missing]
    t = threading.Thread(
        target=_install_packages,
        args=(pip_names, progress_var, status_var, prog_win),
        daemon=True,
    )
    t.start()

    prog_win.grab_set()
    prog_win.mainloop()      # blocks until _install_packages calls root.destroy()

    # Verify everything installed correctly
    still_missing = _check_missing()
    if still_missing:
        names = ", ".join(p for _, p, _ in still_missing)
        messagebox.showerror(
            "Setup Incomplete",
            f"The following packages could not be verified after installation:\n\n"
            f"  {names}\n\n"
            "Try running manually:  pip install " + names
        )
        return False

    return True


