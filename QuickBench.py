def detect_apple_silicon_gpu_cores(cpu_name):
    if not cpu_name:
        return None
    patterns = [
        (r"M1 Max",   "24-core or 32-core GPU"),
        (r"M1 Pro",   "14-core or 16-core GPU"),
        (r"M1 Ultra", "48-core or 64-core GPU"),
        (r"M1\b",     "7-core or 8-core GPU"),
        (r"M2 Max",   "30-core or 38-core GPU"),
        (r"M2 Pro",   "16-core or 19-core GPU"),
        (r"M2 Ultra", "60-core or 76-core GPU"),
        (r"M2\b",     "8-core or 10-core GPU"),
        (r"M3 Max",   "30-core or 40-core GPU"),
        (r"M3 Pro",   "14-core or 18-core GPU"),
        (r"M3\b",     "8-core or 10-core GPU"),
        (r"M4 Max",   "32-core or 40-core GPU"),
        (r"M4 Pro",   "16-core or 20-core GPU"),
        (r"M4\b",     "10-core GPU"),
    ]
    for pattern, label in patterns:
        if re.search(pattern, cpu_name):
            return label
    return None


