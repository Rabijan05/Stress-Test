def _ui_font(size=11, weight="normal"):
    # Use fonts that are guaranteed to resolve in every Tk version on each OS.
    # "SF Pro" requires Tk 8.6.12+ and may silently fall back on older builds;
    # "Helvetica" is always present in macOS Tk via the Aqua backend.
    if sys.platform == "darwin":
        face = "Helvetica"
    elif os.name == "nt":
        face = "Segoe UI"     # available on Vista+ (all supported Windows)
    else:
        face = "Sans"         # universal Pango/Fontconfig alias on all Linux DEs
    return (face, size, weight) if weight != "normal" else (face, size)


def _mono_font(size=11):
    if sys.platform == "darwin":
        return ("Menlo", size)          # ships with macOS 10.6+
    if os.name == "nt":
        return ("Consolas", size)       # ships with Vista+
    return ("Courier New", size)        # universally available on Linux


