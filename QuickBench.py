def bytes_to_gb_string(num_bytes):
    if not num_bytes:
        return "Unknown"
    gib = num_bytes / (1024 ** 3)
    rounded = round(gib)
    if abs(gib - rounded) < 0.2:
        return f"{rounded} GB"
    return f"{gib:.1f} GB"


def mhz_to_string(mhz):
    if not mhz:
        return "Unknown"
    try:
        mhz = float(mhz)
    except Exception:
        return str(mhz)
    if mhz >= 1000:
        return f"{mhz / 1000:.2f} GHz"
    if mhz < 100:
        return "Unknown"
    return f"{int(round(mhz))} MHz"


def normalize_cpu_frequency(max_mhz, current_mhz):
    if max_mhz and max_mhz > 100:
        return mhz_to_string(max_mhz)
    if current_mhz and current_mhz > 100:
        return mhz_to_string(current_mhz)
    return "Unknown"


