    # ------------------------------------------------------------------
    # Close
    # ------------------------------------------------------------------

    def _on_close(self):
        if self.worker.is_running():
            should_close = messagebox.askyesno(
                APP_NAME, "A benchmark is still running. Stop it and quit?")
            if not should_close:
                return
            self.worker.cancel()
            # Cancel the pending timer tick so it doesn't fire on a dead root.
            if self._timer_after_id is not None:
                try:
                    self.root.after_cancel(self._timer_after_id)
                except Exception:
                    pass
            # FIX: wait up to 3 s in a daemon thread for worker processes to
            # exit cleanly before destroying the root. 300 ms was far too short
            # and could leave zombie processes behind.
            def _wait_and_destroy():
                self.worker.join(timeout=3.0)
                # Schedule destroy on the main thread — calling Tk methods
                # from a background thread causes NSView assertion crashes on
                # macOS and undefined behaviour on other platforms.
                try:
                    self.root.after(0, self.root.destroy)
                except Exception:
                    pass
            threading.Thread(target=_wait_and_destroy, daemon=True).start()
            return
        self.root.destroy()


