# System detection  – runs in a background thread (see QuickBenchApp.__init__)
# FIX: previously called on the main thread, blocking the UI for several
# seconds while system_profiler / WMI queries ran.
# ---------------------------------------------------------------------------

def _get_cpu_name():
    """Return a human-readable CPU name across all platforms."""
    # ── Windows ───────────────────────────────────────────────────────────────
    if os.name == "nt":
        # 1. Try Windows registry (fastest, no subprocess needed)
        try:
            import winreg
            key = winreg.OpenKey(
                winreg.HKEY_LOCAL_MACHINE,
                r"HARDWARE\DESCRIPTION\System\CentralProcessor\0"
            )
            name, _ = winreg.QueryValueEx(key, "ProcessorNameString")
            winreg.CloseKey(key)
            name = name.strip()
            if name and "Family" not in name:
                return name
        except Exception:
            pass

        # 2. Try wmic (available on most Windows installs)
        try:
            result = run_command(
                ["wmic", "cpu", "get", "name", "/value"], timeout=8
            )
            if result:
                for line in result.splitlines():
                    if line.lower().startswith("name="):
                        name = line.split("=", 1)[1].strip()
                        if name and "Family" not in name:
                            return name
        except Exception:
            pass

        # 3. Try PowerShell Win32_Processor
        try:
            name = run_powershell(
                "(Get-CimInstance Win32_Processor | Select-Object -First 1 -ExpandProperty Name)"
            )
            if name and "Family" not in name:
                return name.strip()
        except Exception:
            pass

    # ── macOS ─────────────────────────────────────────────────────────────────
    elif sys.platform == "darwin":
        name = run_command(["sysctl", "-n", "machdep.cpu.brand_string"])
        if name:
            return name.strip()

    # ── Linux ─────────────────────────────────────────────────────────────────
    else:
        try:
            with open("/proc/cpuinfo", encoding="utf-8") as f:
                for line in f:
                    if line.lower().startswith("model name"):
                        return line.split(":", 1)[1].strip()
        except Exception:
            pass

    # ── Universal fallback ────────────────────────────────────────────────────
    raw = platform.processor()
    # Hide the ugly raw CPUID string Windows returns
    if raw and "Family" not in raw and "Stepping" not in raw:
        return raw
    return "Unknown CPU"


def detect_system_profile():
    vm = PSUTIL.virtual_memory()
    cpu_freq = PSUTIL.cpu_freq()

    info = {
        "model": "Unknown Model",
        "cpu": _get_cpu_name(),
        "gpu": "Unknown GPU",
        "ram": bytes_to_gb_string(vm.total),
        "ram_type": "Unknown",
        "ram_speed": "Unknown",
        "cpu_cores_physical": PSUTIL.cpu_count(logical=False) or multiprocessing.cpu_count(),
        "cpu_cores_logical":  PSUTIL.cpu_count(logical=True)  or multiprocessing.cpu_count(),
        "cpu_frequency": normalize_cpu_frequency(
            getattr(cpu_freq, "max",     0) if cpu_freq else 0,
            getattr(cpu_freq, "current", 0) if cpu_freq else 0,
        ),
        "gpu_vram": "Unknown",
        "gpu_cores": "Unknown",
        "platform_name": f"{platform.system()} {platform.release()}",
        "label": "",
    }

    if sys.platform == "darwin":
        model  = run_command(["sysctl", "-n", "hw.model"])
        cpu    = run_command(["sysctl", "-n", "machdep.cpu.brand_string"])
        ram    = run_command(["sysctl", "-n", "hw.memsize"])
        sp_hw  = run_command(["system_profiler", "SPHardwareDataType"])
        sp_gpu = run_command(["system_profiler", "SPDisplaysDataType"])
        sp_mem = run_command(["system_profiler", "SPMemoryDataType"])

        if model:
            info["model"] = model

        if cpu:
            info["cpu"] = cpu
        elif sp_hw:
            for line in sp_hw.splitlines():
                clean = line.strip()
                if clean.startswith("Chip:"):
                    info["cpu"] = clean.split(":", 1)[1].strip()
                    break
                if clean.startswith("Processor Name:"):
                    info["cpu"] = clean.split(":", 1)[1].strip()
                    break

        if ram.isdigit():
            info["ram"] = bytes_to_gb_string(int(ram))

        if sp_hw:
            for line in sp_hw.splitlines():
                clean = line.strip()
                if clean.startswith("Total Number of Cores:"):
                    m = re.search(r"(\d+)", clean)
                    if m:
                        cores = int(m.group(1))
                        info["cpu_cores_physical"] = cores
                        info["cpu_cores_logical"]  = max(info["cpu_cores_logical"], cores)
                elif clean.startswith("Memory:"):
                    info["ram"] = clean.split(":", 1)[1].strip()

        gpu_names  = []
        vram_names = []
        gpu_core_label = detect_apple_silicon_gpu_cores(info["cpu"])
        if gpu_core_label:
            info["gpu_cores"] = gpu_core_label

        if sp_gpu:
            for line in sp_gpu.splitlines():
                clean = line.strip()
                if clean.startswith("Chipset Model:"):
                    gpu = clean.split(":", 1)[1].strip()
                    if gpu and gpu not in gpu_names:
                        gpu_names.append(gpu)
                elif clean.startswith("VRAM (Dynamic, Max):") or \
                        clean.startswith("VRAM (Total):"):
                    vram = clean.split(":", 1)[1].strip()
                    if vram and vram not in vram_names:
                        vram_names.append(vram)
