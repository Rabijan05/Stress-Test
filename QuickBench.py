class ScrollableFrame(ttk.Frame):
    def __init__(self, master):
        super().__init__(master)
        self.canvas    = tk.Canvas(self, highlightthickness=0)
        self.scrollbar = ttk.Scrollbar(self, orient="vertical", command=self.canvas.yview)
        self.inner     = ttk.Frame(self.canvas)

        self.inner.bind(
            "<Configure>",
            lambda e: self.canvas.configure(scrollregion=self.canvas.bbox("all"))
        )

        self.window_id = self.canvas.create_window((0, 0), window=self.inner, anchor="nw")
        self.canvas.configure(yscrollcommand=self.scrollbar.set)

        self.canvas.grid(row=0, column=0, sticky="nsew")
        self.scrollbar.grid(row=0, column=1, sticky="ns")
        self.rowconfigure(0, weight=1)
        self.columnconfigure(0, weight=1)

        self.canvas.bind("<Configure>", self._on_canvas_configure)

        # Bind scroll events globally but only act when the pointer is
        # geometrically within the canvas area. This is more reliable than
        # <Enter>/<Leave> because child widgets inside `inner` swallow those
        # events, causing scroll to silently stop on Linux and Windows whenever
        # the pointer moves over a Label, Button, or Entry widget.
        if sys.platform == "darwin":
            self.canvas.bind_all("<MouseWheel>", self._on_mousewheel_mac)
        else:
            self.canvas.bind_all("<MouseWheel>", self._on_mousewheel_win)
        self.canvas.bind_all("<Button-4>", self._on_mousewheel_linux)
        self.canvas.bind_all("<Button-5>", self._on_mousewheel_linux)

    def _pointer_in_canvas(self, event):
        """Return True only if the pointer is within our canvas boundaries."""
        try:
            cx = self.canvas.winfo_rootx()
            cy = self.canvas.winfo_rooty()
            cw = self.canvas.winfo_width()
            ch = self.canvas.winfo_height()
            return cx <= event.x_root <= cx + cw and cy <= event.y_root <= cy + ch
        except Exception:
            return False

    def _on_canvas_configure(self, event):
        self.canvas.itemconfigure(self.window_id, width=event.width)

    # Pixel-accurate smooth scrolling via yview_moveto — same mechanism as
    # dragging the scrollbar, so motion is perfectly fluid on all platforms.
    def _scroll_by_pixels(self, pixels):
        bbox = self.canvas.bbox("all")
        if not bbox:
            return
        content_height = bbox[3] - bbox[1]
        if content_height <= 0:
            return
        current_top = self.canvas.yview()[0]
        self.canvas.yview_moveto(current_top + pixels / content_height)

    def _on_mousewheel_mac(self, event):
        if event.delta and self._pointer_in_canvas(event):
            self._scroll_by_pixels(-event.delta)

    def _on_mousewheel_win(self, event):
        if self._pointer_in_canvas(event):
            pixels = -(event.delta / 120) * 40
            self._scroll_by_pixels(pixels)

    def _on_mousewheel_linux(self, event):
        if not self._pointer_in_canvas(event):
            return
        if event.num == 4:
            self._scroll_by_pixels(-40)
        elif event.num == 5:
            self._scroll_by_pixels(40)


